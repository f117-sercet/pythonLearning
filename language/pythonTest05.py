# 列表的基本操作

test = [1, 2, 34, 55, 66, 99, 4]

# 在列表尾部增加元素
print(test)

# 列表清空
test.clear();

# 复制生成另外一个列表
test.copy();

# 统计指定元素个数
test.count(4)
print(test.count(4))

# 两个列表合并

test.extend()

# 返回指定元素下标
test.index(4)

# 在指定位置插入元素
test.insert(1, 4)

# 删除并返回指定下标对应的元素
test.pop() ;

# 反转列表元素顺序
test.reverse()

# 对列表元素进行排序
test.sort()
